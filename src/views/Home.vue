        position: myCenter
      });
      marker.setMap(map);
    },
    carousel() {
      var i;
      var x = document.getElementsByClassName("mySlides");
      for (i = 0; i < x.length; i++) {
        x[i].style.display = "none";
      }
      this.myIndex++;
      if (this.myIndex > x.length) {
        this.myIndex = 1;
      }
      x[this.myIndex - 1].style.display = "block";
      setTimeout(this.carousel, 5000);
    }
